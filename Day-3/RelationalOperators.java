public class RelationalOperators {
    public static void main(String[] args) {
        //Relational Operators
        int a = 6, b = 5;

        // Equal operator ==
        boolean result = (a == b);
        System.out.println(result);

        // Not Equal operator !=
        result = (a != b);
        System.out.println(result);

        // Greater than operator >
        result = (a > b);
        System.out.println(result);

        // Less than operator <
        result = (a < b);
        System.out.println(result);

        // Greater than or equal to operator >=
        result = (a >= b);
        System.out.println(result);

        // Less than or equal to operator <=
        result = (a <= b);
        System.out.println(result);
    }
}
